#include "Proyectil.h"

///////////////////////////////////////////////////////////////////////////
Proyectil::Proyectil(int x, int y)
{
  m_x = x;
  m_y = y;
}

///////////////////////////////////////////////////////////////////////////
void Proyectil::update()
{
  // El proyectil podrá subir hasta el límite de la pantalla.
  if (m_y >= 1) m_y--;
}

///////////////////////////////////////////////////////////////////////////
void Proyectil::draw()
{
  // Dibujamos el proyectil.
  mvaddch(m_y, m_x, '^');
}